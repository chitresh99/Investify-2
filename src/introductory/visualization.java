    private void visualizeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_visualizeActionPerformed
        int s1 = Integer.parseInt(txtnecessary.getText()); //Parsing the text inputs for necessary, unnecessary, savings, and can't classify values as integers.
        int s2 = Integer.parseInt(txtunnecessary.getText());
        int s3 = Integer.parseInt(txtsavings.getText());
        int s4 = Integer.parseInt(txtcantclassify.getText());

        DefaultCategoryDataset dataset = new DefaultCategoryDataset(); //Creating a dataset for the bar chart
        dataset.setValue(s1, "", "Necessary");
        dataset.setValue(s2, "", "Un-Necessary");
        dataset.setValue(s3, "", "Savings");
        dataset.setValue(s4, "", "Can't classify");
         
        // Creating a bar chart
        JFreeChart chart = ChartFactory.createBarChart("Visual", "", "", dataset, PlotOrientation.HORIZONTAL, false, false, false);
        CategoryPlot catPlot = chart.getCategoryPlot();
        catPlot.setRangeGridlinePaint(Color.BLACK);
        
        // Creating a chart panel to hold the chart
        ChartPanel chartPanel = new ChartPanel(chart);
        vpanel.removeAll();
        vpanel.add(chartPanel, BorderLayout.CENTER);
        vpanel.validate();
    }//GEN-LAST:event_visualizeActionPerformed

