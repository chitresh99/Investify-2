    private void searchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchActionPerformed
        String date = searchbar.getText(); // Get the date from the search bar

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/investify", "root", "CHIR2502004|");
            String query = "SELECT * FROM expensecategorization WHERE date = ?"; //selecting the tables from expense categorization
            pst = con.prepareStatement(query);
            pst.setString(1, date);  //Setting the date parameter for the prepared statement
            rs = pst.executeQuery(); //Executing the query and storing the result in the ResultSet
            
            // If a row is found for the provided date, update the text fields with the corresponding values
            if (rs.next()) {
                txtnecessary.setText(rs.getString("necessaryexp"));
                txtunnecessary.setText(rs.getString("unncessaryexp"));
                txtsavings.setText(rs.getString("savings"));
                txtcantclassify.setText(rs.getString("cantclassify"));
            }
        } catch (ClassNotFoundException | SQLException e) {

            e.printStackTrace();
        }
    }//GEN-LAST:event_searchActionPerformed

